	<h1><center>Gunung Tangkuban Perahu</center></h1>
	<img src="Gambar/TangkubanPerahu1.jpg" height="30%" width="50%"> 
	<p>Kawah Tangkuban Perahu merupakan salah satu objek wisata yang paling populer di daerah Bandung dan sekitarnya . Wajar saja sih, hal ini dikarenakan pemandangan seputar gunung dan kawah Tangkuban Perahu yang indah dan memanjakan mata. Gunung tangkuban perahu seolah-olah menjadi tempat yang wajib anda kunjungi kalau berlibur di Bandung. Tempatnya mudah dijangkau dan cukup unik lho. Kawah utama disini, kawah ratu, luas sekali. anda bisa menikmati pemandangan sekitarnya langsung dari bibir kawah. Legenda Tangkuban Perahu
	Gunung Tangkuban Perahu dikenal dalam legenda Jawa barat yang terkenal, legenda Sangkuriang dan Dayang sumbi. Gunung ini disebut sebagai tangkuban perahu ( perahu terbalik) karena memang jika dilihat dari jauh terlihat seperti perahu yang terbalik. Anda bisa melihat bentuk ini dengan jelas dari daerah Maribaya Lembang.Cerita rakyat tangkuban perahu ini cukup dikenal di Jawa barat. Cerita tangkuban perahu mengisahkan tentang Sangkuriang, seorang anak yang jatuh cinta pada ibunya, Dayang Sumbi. Agar keinginan Sangkuriang tidak terlaksana, Dayang sumbi mengajukan syarat untuk dibuatkan telaga dan perahu dalam satu malam. Dengan disertai usaha dari Dayang Sumbi, akhirnya sangkuriang gagal memenuhi syarat tersebut. Sangkuriang kemudian melampiaskan kemarahannya dengan menendang perahu yang baru setengah jadi.
	Menurut legenda, perahu yang ditendang oleh Sangkuriang mendarat terbalik, dan menjadi gunung tangkuban perahu. Tunggul dari pohon yang ditebang dan dibuat menjadi perahu berubah menjadi gunung bukit tunggul.</p>
	
	<h2>Daya Tarik Wisata Gunung Tangkuban Perahu</h2>
	<p>Tidak terdapat hotel atau tempat menginap didalam kompleks wisata gunung Tangkuban Perahu . Tapi anda bisa dengan mudah menemukan penginapan dan hotel yang tidak jauh dari lokasi. Anda bisa mencari penginapan di daerah Lembang yang terletak dekat dengan lokasi.
	Jangan khawatir jika merasa lapar atau haus, banyak pedagang dan kantin tempat istirahat yang menyediakan makanan dan minuman. Kelengkapan lain seperti Masjid dan kamar kecil tersedia cukup banyak dan bersih . Sarana parkir juga tersedia cukup luas. Jika anda membawa kendaraan pribadi, anda bisa langsung parkir di dekat kawah tangkuban perahu sehingga tidak perlu berjalan jauh untuk menuju lokasi. Jika rombongan dengan kendaraan besar/bis, area parkir khusus mobil besar terletak kurang lebih 200 meter sebelum kawah ratu.</p>
	
	<h2>Harga & Tarif Objek Wisata Gunung Tangkuban Perahu</h2>
	<p>Tiket masuk kawah areal tangkuban perahu kami pikir termasuk relatif mahal. Harga tiket tangkuban perahu dimulai dari 20 ribu pada hari biasa dan 30 ribu di weekend dan hari libur untuk pengunjung Nusantara. Sedangkan untuk pengunjung manca negara mulai dari 200 ribu di hari biasa sampai dengan 300 ribu di weekend. Terakhir berkunjung kesini hari Rabu 11 Desember 2015 ( libur pilkada serentak),kami menghabiskan Rp 77 ribu rupiah untuk tiket 2 orang+motor. Dan masih harus ditambah lagi dengan tiket parkir sebesar Rp 5000.
